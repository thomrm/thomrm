<p>Chat badges for Moonbeam livestreaming platform. The task was to <span class="highlight">create two of the initial badges for users in chat</span>. Work was done over several days and in collaboration with Moonbeam's designer. An initial round of rough concepts were delivered before a final direction was agreed on and&nbsp;executed.</p>

